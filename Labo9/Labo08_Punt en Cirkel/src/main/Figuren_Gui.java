    public void setKleuren() {
        String color1 = jComboBox2.getSelectedItem() + "";
        String color2 = jComboBox3.getSelectedItem() + "";
        if ("zwart".equals(color1)) {
            kleur = Color.BLACK;

        } else if ("wit".equals(color1)) {
            kleur = Color.WHITE;

        } else if ("rood".equals(color1)) {
            kleur = Color.RED;

        } else if ("oranje".equals(color1)) {
            kleur = Color.ORANGE;

        } else if ("geel".equals(color1)) {
            kleur = Color.YELLOW;

        }
        if ("zwart".equals(color2)) {
            kleur2 = Color.BLACK;

        } else if ("wit".equals(color2)) {
            kleur2 = Color.WHITE;

        } else if ("rood".equals(color2)) {
            kleur2 = Color.RED;

        } else if ("oranje".equals(color2)) {
            kleur2 = Color.ORANGE;

        } else if ("geel".equals(color2)) {
            kleur2 = Color.YELLOW;

        }

    }

    public void setMidden(JPanel j) {

        //dus we zetten het midden van de ene fram op het midden van de andere frame 
        int panelX = j.getParent().getWidth() / 2;

        int panelY = j.getParent().getHeight() / 2;

        int p1 = j.getWidth() / 2;
        int p2 = j.getHeight() / 2;

        j.setLocation(panelX - p1, panelY - p2);

    }
