    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String figuur = jComboBox1.getSelectedItem() + "";

        int grootte = Integer.parseInt(jTextField1.getText());
        int dikteRand = (Integer) jSpinner1.getValue();

        setKleuren();

        if (figuur.equals("Vierkant")) {

            jPanel1.setSize(240, 240);
            Vierkant v = new Vierkant(grootte);
            newJPanel1.setEnabled(false);
            newJPanel2.setEnabled(false);
            newJPanel1.setVisible(false);
            newJPanel2.setVisible(false);

            jPanel4.setSize(grootte, grootte); //MIDDEN
            jPanel3.setSize(grootte + dikteRand, grootte + dikteRand); //RAND
            jPanel3.setBackground(kleur);
            jPanel4.setBackground(kleur2);

            for (int i = 0; i < 2; i++) {
                setMidden(jPanel3);//RAND
                setMidden(jPanel4);//MIDDEN
            }
            jPanel4.setEnabled(true);
            jPanel3.setEnabled(true);
            jPanel4.setVisible(true);
            jPanel3.setVisible(true);
        } else if (figuur.equals("Cirkel")) {
            jPanel1.setSize(240, 240);

            newJPanel2.setEnabled(true);
            newJPanel1.setEnabled(true);
            newJPanel2.setVisible(true);
            newJPanel1.setVisible(true);

            Cirkel c = new Cirkel(grootte);

            for (int i = 0; i < 2; i++) {

                setMidden(newJPanel1);//RAND
                setMidden(newJPanel2);//MIDDEN
                newJPanel2.setSize(grootte, grootte); //MIDDEN
                newJPanel1.setSize(grootte + dikteRand, grootte + dikteRand); //RAND
                this.newJPanel2.setKleur(kleur);
                this.newJPanel1.setKleur(kleur2);
                newJPanel1.setBackground(new Color(0, 0, 0, 0));
                newJPanel2.setBackground(new Color(0, 0, 0, 0));
            }

            jPanel3.setEnabled(false);
            jPanel4.setEnabled(false);
            jPanel3.setVisible(false);
            jPanel4.setVisible(false);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

